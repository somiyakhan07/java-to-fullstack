// Demonstrate Relational Operators in java 

public class RelationalOperators {
    public static void main(String args[]) {
        int a = 10;
        int b = 20;

        System.out.println("a == b: " + (a == b)); // Equal to operators
        System.out.println("a != b: " + (a != b)); // Not equal to operator
        System.out.println("a > b: " + (a > b)); // Greater than operator
        System.out.println("a < b: " + (a < b)); // Less than operators
        System.out.println("a >= b: " + (a >= b)); // Greater than or equal to operator
        System.out.println("a <= b: " + (a <= b)); // Lesser than or equal to operator
    }

}

