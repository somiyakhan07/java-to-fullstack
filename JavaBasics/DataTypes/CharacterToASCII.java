// Demonstrates conversion of a character to its ASCII value.

public class CharacterToASCII {
    public static void main(String args[]) {
        char ch = 'A'; // charater value

        int ascii = ch;
        // Java automatically converts the character to
        // its corresponding ASCII(Unicode) integer value.

        System.out.println(ch);
        System.out.println(ascii);

    }

}

